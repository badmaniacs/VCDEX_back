package com.myapp.vcdex.vo;

public class CardVO {
    private int price;
    private String link;
    private int no;
    private String name;
    private String clock;
    private String memory;
    private String memorytype;
    private String memorybit;
    private String memoryspeed;
    private String power;
    private String image;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClock() {
        return clock;
    }

    public void setClock(String clock) {
        this.clock = clock;
    }

    public String getMemory() {
        return memory;
    }

    public void setMemory(String memory) {
        this.memory = memory;
    }

    public String getMemorytype() {
        return memorytype;
    }

    public void setMemorytype(String memorytype) {
        this.memorytype = memorytype;
    }

    public String getMemorybit() {
        return memorybit;
    }

    public void setMemorybit(String memorybit) {
        this.memorybit = memorybit;
    }

    public String getMemoryspeed() {
        return memoryspeed;
    }

    public void setMemoryspeed(String memoryspeed) {
        this.memoryspeed = memoryspeed;
    }

    public String getPower() {
        return power;
    }

    public void setPower(String power) {
        this.power = power;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
